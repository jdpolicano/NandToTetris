echo "\nBegining script"
EXEC=./translator
ROOT_DIR=../examples/Virtual
MEMORY_ACCESS=MemoryAccess
BASIC=BasicTest
POINTER=PointerTest
STATIC=StaticTest

BASIC_INPUT=$ROOT_DIR/$MEMORY_ACCESS/$BASIC/$BASIC.vm
BASIC_OUTPUT=$ROOT_DIR/$MEMORY_ACCESS/$BASIC/$BASIC.asm
