make

echo "\nTesting Basic\n"
$EXEC $BASIC_INPUT $BASIC_OUTPUT
echo "\nTesting Pointer\n"
$EXEC $POINTER_INPUT $POINTER_OUTPUT
echo "\nTesting INPUT\n"
$EXEC $STATIC_INPUT $STATIC_OUTPUT

echo "Script end"